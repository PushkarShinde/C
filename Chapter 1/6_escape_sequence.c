#include <stdio.h>

/*
Here are some common escape sequences in C:
\n : Newline
\t : Horizontal tab
\\ : Backslash
\" : Double quote
\' : Single quote
\r : Carriage return
\b : Backspace
\f : Form feed
\v : Vertical tab
\a : Alert (bell)
\? : Question mark
\0 : Null character (NUL)
*/

int main() 
{
    printf("Hello, World!\n");   // Newline
    printf("Tabbed\ttext\n");    // Tab
    printf("Backslash: \\\n");   // Backslash
    printf("Double quote: \"\n");// Double quote
    return 0;
}
   